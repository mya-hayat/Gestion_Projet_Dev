package Models;

public class Tache {
	
	private int idTache;
	private String descTache;
	private String avancement;
	private String dateTache;
	private int idDeveloper;
	private int idService;
	
	public int getIdTache() {
		return idTache;
	}
	public void setIdTache(int idTache) {
		this.idTache = idTache;
	}
	public String getDescTache() {
		return descTache;
	}
	public void setDescTache(String descTache) {
		this.descTache = descTache;
	}
	public String getAvancement() {
		return avancement;
	}
	public void setAvancement(String avancement) {
		this.avancement = avancement;
	}
	public String getDateTache() {
		return dateTache;
	}
	public void setDateTache(String dateTache) {
		this.dateTache = dateTache;
	}
	public int getIdDeveloper() {
		return idDeveloper;
	}
	public void setIdDeveloper(int idDeveloper) {
		this.idDeveloper = idDeveloper;
	}
	public int getIdService() {
		return idService;
	}
	public void setIdService(int idService) {
		this.idService = idService;
	}
	
	

}

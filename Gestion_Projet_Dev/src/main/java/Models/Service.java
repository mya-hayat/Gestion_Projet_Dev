package Models;

public class Service {

	private int idService;
	private String descService;
	private int dureeJoursService;
	private int idProjet;
	
	public int getIdService() {
		return idService;
	}
	public void setIdService(int idService) {
		this.idService = idService;
	}
	public String getDescService() {
		return descService;
	}
	public void setDescService(String descService) {
		this.descService = descService;
	}
	public int getDureeJoursService() {
		return dureeJoursService;
	}
	public void setDureeJoursService(int dureeJoursService) {
		this.dureeJoursService = dureeJoursService;
	}
	public int getIdProjet() {
		return idProjet;
	}
	public void setIdProjet(int idProjet) {
		this.idProjet = idProjet;
	}
	
	
}
